#include <stdio.h>

int main() {
    float score, bonus; // ใช้ float เพื่อรองรับคะแนนที่เป็นทศนิยม

    // รับคะแนนสอบจากผู้ใช้
    printf("กรอกคะแนนสอบ Midterm: ");
    scanf("%f", &score); // รับค่าคะแนนเป็นทศนิยม

    // ตรวจสอบว่าคะแนนมากกว่า 50 หรือไม่
    if (score > 50) {
        bonus = score * 0.05;  // คำนวณโบนัส 5%
        score += bonus;        // บวกโบนัสเข้าไป
    }

    // แสดงผลคะแนนรวม
    printf("คะแนนรวมหลังคำนวณ: %.2f\n", score); // %.2f แสดงทศนิยม 2 ตำแหน่ง

    // แสดงข้อความปิดท้าย
    printf("End of evaluation\n");

    return 0; // คืนค่า 0 เพื่อบอกว่าการทำงานสำเร็จ
}
